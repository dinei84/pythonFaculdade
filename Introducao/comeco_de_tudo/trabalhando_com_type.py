a = input('Digite algo: ')
print('O tipo primitivo de ', a, ' é: ', type(a))
print('Só tem espaços? ', a.isspace())
print('É um numero? ', a.isnumeric())
print('É alfabetico? ', a.isalpha())
print('É alfanumerico? ', a.isalnum()) #fommato de letras e numeros
print('Esta em minusculas? ', a.islower())
print('Esta em maiusculas? ', a.isupper())
print('Esta capitalizada? ', a.istitle()) #formato de titulo



 